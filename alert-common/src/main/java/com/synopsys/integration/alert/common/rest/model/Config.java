package com.synopsys.integration.alert.common.rest.model;

public abstract class Config extends AlertSerializableModel {
    private String id;

    public Config() {
    }

    public Config(final String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(final String id) {
        this.id = id;
    }

}
